package pojopackage;

public class Tags {
	
	private String id;

    private String name;

    public void setId(String tagid){
        this.id = tagid;
    }
    public String getId(){
        return this.id;
    }
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return this.name;
    }
}

